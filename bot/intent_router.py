_CLASSIFIER_SYSTEM = (
    "Ты — классификатор интентов для контент-бота маркетолога.\n"
    "Пользователь написал запрос в свободной форме. Определи что он хочет.\n\n"
    "Возможные агенты:\n"
    + "\n".join(f"- {k}: {v}" for k, v in AGENT_DESCRIPTIONS.items())
    + "\n- chat: общий вопрос про маркетинг, не подходит ни под один агент выше\n\n"
    "Верни ТОЛЬКО валидный JSON без markdown-блоков:\n"
    "{\"agent\": \"<ключ>\", \"topic\": \"<тема/суть запроса в 3-7 словах>\", \"confidence\": <0.0-1.0>, \"urgency\": false}\n\n"
    "urgency=true когда: пользователь явно просит генерировать немедленно "
    "(\"сделай\", \"давай\", \"go ahead\", \"hazlo ya\"), "
    "выражает усталость от вопросов, или уже дал достаточно контекста. "
    "urgency=false — обычный запрос.\n\n"
    "Правила confidence:\n"
    "- 0.9+: запрос однозначно про конкретный формат контента\n"
    "- 0.7-0.9: очень вероятно нужен этот агент\n"
    "- 0.5-0.7: неуверен\n"
    "- <0.5: непонятно → agent=\"chat\"\n\n"
    "Примеры:\n"
    "\"сделай хук для рилса про выгорание\" → {\"agent\":\"reels_short\",\"topic\":\"хук про выгорание\",\"confidence\":0.95}\n"
    "\"напиши пост\" → {\"agent\":\"post\",\"topic\":\"пост (тема не указана)\",\"confidence\":0.75}\n"
    "\"что такое воронка\" → {\"agent\":\"chat\",\"topic\":\"вопрос про воронку\",\"confidence\":0.9}\n"
)


@dataclass
class IntentResult:
    agent:      str
    topic:      str
    confidence: float
    urgency:    bool = False  # ← добавить
    raw:        str = ""


async def classify_intent(text: str, profile: dict | None = None) -> IntentResult:
    from llm import complete

    profile_ctx = ""
    if profile:
        parts = []
        if profile.get("niche"):    parts.append(f"Ниша: {profile['niche']}")
        if profile.get("audience"): parts.append(f"Аудитория: {profile['audience']}")
        if parts:
            profile_ctx = "\n\nКонтекст профиля: " + ", ".join(parts)

    user_prompt = f"Запрос пользователя: «{text}»{profile_ctx}"
    raw = ""
    try:
        raw = await complete(
            system=_CLASSIFIER_SYSTEM,
            user=user_prompt,
            model_key="claude",
            max_tokens=200,
            temperature=0.1,    # детерминированная классификация
        )
        raw = raw.strip()
        raw = re.sub(r"```(?:json)?|```", "", raw).strip()
        if raw and not raw.rstrip().endswith("}"):
            raw = raw.rstrip().rstrip(",") + "}"
        data = json.loads(raw)
        return IntentResult(
            agent=data.get("agent", "chat"),
            topic=data.get("topic", text[:50]),
            confidence=float(data.get("confidence", 0.5)),
            urgency=bool(data.get("urgency", False)),
            raw=raw,
        )
    except Exception as e:
        logger.warning(f"Intent classifier failed: {e} | raw={raw!r}")
        return IntentResult(agent="chat", topic=text[:50], confidence=0.0)


# ── One-shot генератор ─────────────────────────────────────────────────────────

async def oneshot_generate(
    agent_key: str,
    user_text: str,
    profile: dict | None = None,
) -> str:
    """
    Быстрая генерация без интервью.
    temperature=0.88 + presence_penalty=0.4 — против повторяющихся структур.
    """
    from llm import complete

    system = _ONESHOT_PROMPTS.get(agent_key, _ONESHOT_PROMPTS["post"])

    profile_ctx = ""
    if profile:
        parts = []
        if profile.get("niche"):    parts.append(f"Ниша: {profile['niche']}")
        if profile.get("audience"): parts.append(f"Аудитория: {profile['audience']}")
        if profile.get("tone"):     parts.append(f"Тон: {profile['tone']}")
        if parts:
            profile_ctx = "\n\n[Профиль]\n" + "\n".join(parts)

    user_prompt = f"Запрос: «{user_text}»{profile_ctx}\n\nСгенерируй результат прямо сейчас."

    # Для oneshot используем complete_long — больше токенов, presence_penalty
    from llm import complete_long
    return await complete_long(
        system=system,
        user=user_prompt,
        model_key="claude",
        temperature=0.88,
        presence_penalty=0.4,
    )


