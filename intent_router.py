_CLASSIFIER_SYSTEM = """Ты — классификатор интентов для контент-бота маркетолога.
Пользователь написал запрос в свободной форме. Определи что он хочет.

Возможные агенты:
""" + "\n".join(f"- {k}: {v}" for k, v in AGENT_DESCRIPTIONS.items()) + """
- chat: общий вопрос про маркетинг, не подходит ни под один агент выше

Верни ТОЛЬКО валидный JSON без markdown-блоков:
{"agent": "<ключ>", "topic": "<тема/суть запроса в 3-7 словах>", "confidence": <0.0-1.0>}

Правила confidence:
- 0.9+: запрос однозначно про конкретный формат контента
- 0.7-0.9: очень вероятно нужен этот агент
- 0.5-0.7: неуверен, но склоняюсь к этому
- <0.5: непонятно или это общий вопрос → agent="chat"

Примеры:
"сделай мне хук для рилса про выгорание" → {"agent":"reels_short","topic":"хук для рилса про выгорание","confidence":0.95}
"напиши пост" → {"agent":"post","topic":"пост (тема не указана)","confidence":0.75}
"что такое воронка" → {"agent":"chat","topic":"вопрос про воронку","confidence":0.9}
"хочу прогреть аудиторию перед запуском курса" → {"agent":"warmup","topic":"прогрев перед запуском курса","confidence":0.9}
"""


@dataclass
class IntentResult:
    agent:      str
    topic:      str
    confidence: float
    raw:        str = ""


async def classify_intent(text: str, profile: dict | None = None) -> IntentResult:
    """Быстрый LLM-вызов для определения интента. Возвращает IntentResult."""
    from llm import complete

    profile_ctx = ""
    if profile:
        parts = []
        if profile.get("niche"):    parts.append(f"Ниша: {profile['niche']}")
        if profile.get("audience"): parts.append(f"Аудитория: {profile['audience']}")
        if parts:
            profile_ctx = "\n\nКонтекст профиля: " + ", ".join(parts)

    user_prompt = f"Запрос пользователя: «{text}»{profile_ctx}"

    try:
        raw = await complete(
            system=_CLASSIFIER_SYSTEM,
            user=user_prompt,
            model_key="claude",
            max_tokens=120,
        )
        raw = raw.strip()
        # Убираем возможные markdown-блоки
        raw = re.sub(r"```(?:json)?|```", "", raw).strip()
        data = json.loads(raw)
        return IntentResult(
            agent=data.get("agent", "chat"),
            topic=data.get("topic", text[:50]),
            confidence=float(data.get("confidence", 0.5)),
            raw=raw,
        )
    except Exception as e:
        logger.warning(f"Intent classifier failed: {e} | raw={raw if 'raw' in dir() else '?'}")
        return IntentResult(agent="chat", topic=text[:50], confidence=0.0)


# ─────────────────────────────────────────────────────────────────────────────
# One-shot генератор
# ─────────────────────────────────────────────────────────────────────────────

async def oneshot_generate(
    agent_key: str,
    user_text: str,
    profile: dict | None = None,
) -> str:
    """Генерирует результат сразу, без интервью."""
    from llm import complete

    system = _ONESHOT_PROMPTS.get(agent_key, _ONESHOT_PROMPTS["post"])

    profile_ctx = ""
    if profile:
        parts = []
        if profile.get("niche"):    parts.append(f"Ниша: {profile['niche']}")
        if profile.get("audience"): parts.append(f"Аудитория: {profile['audience']}")
        if profile.get("tone"):     parts.append(f"Тон: {profile['tone']}")
        if parts:
            profile_ctx = "\n\n[Профиль пользователя]\n" + "\n".join(parts)

    user_prompt = f"Запрос: «{user_text}»{profile_ctx}\n\nСгенерируй результат прямо сейчас."

    return await complete(system=system, user=user_prompt, model_key="claude", max_tokens=2000)


