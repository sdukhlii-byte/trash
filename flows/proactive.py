async def maybe_suggest_next(
    update: Update,
    user_id: int,
    agent_key: str,
    delay: float = 0.8,
) -> None:
    """
    Отправляет проактивное предложение следующего шага.

    Вызывается после _after_result() в agents.py и аналогичных местах.
    delay нужен чтобы сообщение пришло после кнопок результата,
    а не одновременно с ними — пользователь успевает прочитать.

    Показывается только если:
    - Пользователь в TRIAL или SUBSCRIBED (не expired)
    - Не чаще одного раза за генерацию (блокировка через Redis TTL)
    """
    import asyncio
    from user_state import get_user_state, has_access
    from db import kv_get, kv_set

    try:
        state = await get_user_state(user_id)
        if not has_access(state):
            return

        # Дедупликация — не показываем чаще раза за 10 мин
        dedup_key = "__proactive_shown__"
        if await kv_get(user_id, dedup_key):
            return
        await kv_set(user_id, dedup_key, "1", ttl=600)

        suggestions = _SUGGESTIONS.get(agent_key, _UNIVERSAL)
        if not suggestions:
            return

        text, btn_label, btn_data = random.choice(suggestions)

        await asyncio.sleep(delay)
        await send(
            update,
            text,
            parse_mode="Markdown",
            reply_markup=kb(
                [f"{btn_label}|{btn_data}"],
                ["← Пропустить|menu_main"],
            ),
        )
    except Exception as e:
        logger.warning(f"maybe_suggest_next failed for {user_id}/{agent_key}: {e}")
