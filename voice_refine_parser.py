"""
voice_refine_parser.py — распознаёт голосовые инструкции доработки.

Фаза 2: голосовой рефайн без потери контекста.
Интегрируется в flows/misc.py → refine_do().
"""
import re
import logging

logger = logging.getLogger(__name__)

# Ключевые паттерны рефайна — маппинг на инструкции трансформации
_REFINE_PATTERNS = [
    # Тональность
    (r"(сделай|стань|будь).{0,10}(мягч|нежн|мяко)",           "сделай мягче"),
    (r"(слишком|очень).{0,10}(официальн|формальн|жёстк)",     "убери официальность"),
    (r"(больше|добавь).{0,20}(подруж|тепл|душевн)",           "вайб подруги"),
    (r"(как.в.сторис|сторисн|разговорн)",                      "разговорнее"),
    (r"(живее|живой|жизненн)",                                  "сделай живее"),
    (r"(проще|попроще|понятнее)",                               "сделай проще"),
    (r"(жёстче|острее|жёстк|агрессивн|заряженн)",             "сделай жёстче"),

    # Содержание
    (r"(слишком|очень).{0,10}(продажн|рекламн|впариваеш)",    "убери продажность"),
    (r"(больше|добавь).{0,15}(личн|от.себя|о.себе|своего)",  "добавь личного"),
    (r"(сделай|стань).{0,10}(короч|кратч|сжато)",             "сделай короче"),
    (r"(другой|другое|иной).{0,15}(угол|взгляд|подход)",      "другой угол"),
    (r"(добавь|усиль).{0,10}(деталь|конкретик|примеры?)",     "добавь деталей"),

    # Структура
    (r"(убери|удали).{0,10}(вопрос)",                          "убери вопросы в конце"),
    (r"(добавь|нужен|нужна).{0,15}(призыв|cta|подписк|ссылк)","добавь призыв к действию"),
    (r"(перепиш|переделай).{0,10}(хук|начал|первы)",          "перепиши хук"),
    (r"(финал|конец|концовк).{0,15}(слаб|плох|скучн)",        "усиль финал"),
]


def detect_refine_intent(text: str) -> str | None:
    """
    Проверяет текст на паттерны рефайна.
    Возвращает человекочитаемую инструкцию или None.
    """
    text_l = text.lower().strip()
    for pattern, label in _REFINE_PATTERNS:
        if re.search(pattern, text_l):
            logger.info("[voice_refine_parser] matched: %r → %s", text_l[:60], label)
            return label
    return None


