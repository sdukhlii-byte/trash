_DEFAULT_INTEL = {
    "works": (
        "Универсальные принципы которые работают в любой нише:\n"
        "• Конкретность: цифры, имена, даты — вместо абстракций\n"
        "• Честность: признание ошибок и провалов строит больше доверия чем успех\n"
        "• Специфика аудитории: говори о их конкретных болях, не о теме вообще\n"
        "• Нестандартный угол: что все делают — переверни"
    ),
    "avoid": "",
}


def get_niche_intel(niche: str) -> dict:
    """Определяет нишу и возвращает intelligence block."""
    if not niche:
        return _DEFAULT_INTEL
    niche_lower = niche.lower()
    for key, intel in _NICHE_INTEL.items():
        for kw in intel.get("keywords", []):
            import re
            if re.search(kw, niche_lower):
                return intel
    return _DEFAULT_INTEL


def build_niche_context(niche: str) -> str:
    """Строит блок для инжекции в system prompt."""
    intel = get_niche_intel(niche)
    parts = []
    if intel.get("works"):
        parts.append(f"ЗНАНИЕ О НИШЕ (используй это при генерации):\n{intel['works']}")
    if intel.get("avoid"):
        parts.append(intel["avoid"])
    if not parts:
        return ""
    return "\n\n" + "\n\n".join(parts)
