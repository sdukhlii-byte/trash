# ── Стрик ─────────────────────────────────────────────────────────────────────

def streak_msg(days: int) -> str:
    if days == 1:
        return "Первый день — хорошее начало 🌱"
    elif days == 3:
        return f"3 дня подряд 🔥 — ты в ритме"
    elif days == 7:
        return f"7 дней подряд 🔥🔥 — это уже привычка"
    elif days == 14:
        return f"2 недели без перерыва 💪 — серьёзно"
    elif days == 30:
        return f"Месяц подряд 🏆 — это уже мастерство"
    elif days % 10 == 0:
        return f"{days} дней подряд 🔥 — не останавливайся"
    else:
        return f"🔥 {days} дней подряд"


