"""
ui/mira_voice.py — живые фразы Миры для всего интерфейса.

Принцип: каждый момент взаимодействия — это реплика живого человека,
а не системное сообщение. Мира варьирует фразы чтобы не было ощущения шаблона.

Использование:
    from ui.mira_voice import greet, menu_prompt, thinking, done
"""
import random
from datetime import datetime, timezone


def _h() -> int:
    return datetime.now(timezone.utc).hour


# ── Приветствия по времени суток ──────────────────────────────────────────────

def greet(name: str = "") -> str:
    h = _h()
    n = f", {name.split()[0]}" if name else ""

    if 5 <= h < 12:
        options = [
            f"Доброе утро{n} ☀️",
            f"Утро{n} — самое время для хорошего контента",
            f"С добрым утром{n}! Что создаём сегодня?",
        ]
    elif 12 <= h < 17:
        options = [
            f"Привет{n} 👋",
            f"О, ты здесь{n}!",
            f"Хорошо что зашла{n}",
        ]
    elif 17 <= h < 22:
        options = [
            f"Вечер добрый{n} 🌙",
            f"Вечернее вдохновение{n}?",
            f"Заканчиваем день с хорошим контентом{n}?",
        ]
    else:
        options = [
            f"Не спится{n}? Создадим что-нибудь 🌙",
            f"Ночное творчество{n} — лучшее творчество",
            f"Полночный контент{n}? Я тут 🌙",
        ]

    return random.choice(options)


# ── Промпты меню ──────────────────────────────────────────────────────────────

