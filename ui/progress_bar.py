# ── Онбординг ─────────────────────────────────────────────────────────────────

def onboarding_progress(step: int, total: int = 3) -> str:
    """🩷🩷⚪ шаг 2 из 3"""
    bar = _bar(step, total, "pink", width=total)
    pct = round(step / total * 100)
    return f"_Шаг {step} из {total}: {bar} {pct}%_"


# ── Триал-таймер ──────────────────────────────────────────────────────────────

def trial_urgency(hours_left: float) -> str:
    if hours_left <= 6:
        bar = _bar(round(hours_left), 6, "red", width=6)
        return f"🔴 *Осталось {round(hours_left)} ч.* {bar}\n_Триал заканчивается — оформи подписку чтобы не потерять прогресс._"
    elif hours_left <= 24:
        bar = _bar(round(hours_left), 24, "orange", width=5)
        return f"🟠 *Остался {round(hours_left)} ч.* {bar} до конца триала."
    else:
        days = round(hours_left / 24)
        bar  = _bar(days, 7, "yellow", width=7)
        return f"⏳ Триал: {bar} {days} дн. осталось"


# ── Счётчик материалов ────────────────────────────────────────────────────────

def materials_count(count: int, goal: int = 10) -> str:
    """Для кабинета/прогресс-экрана."""
    if count == 0:
        return "🤍🤍🤍🤍🤍 ещё ничего не создано"
    color = "green" if count >= goal else "yellow" if count >= goal // 2 else "pink"
    bar   = _bar(min(count, goal), goal, color, width=5)
    return f"{bar} {count} {'материал' if count == 1 else 'материала' if count < 5 else 'материалов'}"
