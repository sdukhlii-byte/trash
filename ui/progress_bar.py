"""
ui/progress_bar.py — прогресс-бары и живые статусы.

v3: нарратив исправлен — «твой стиль», не «голос Миры».
Пользователь должен понимать: бот учится писать КАК ОН, а не настраивает себя.

Палитра по контексту:
  🟣 фиолетовый  — стиль/голос пользователя (творчество, личность)
  🩷 розовый     — онбординг (тепло, приветствие)
  🟡 жёлтый      — прогресс/рост (энергия, движение)
  🟢 зелёный     — завершение/успех
  🔴 красный     — срочность/дедлайн
  🔵 синий       — нейтральный прогресс
"""

# ── Универсальные эмодзи-сеты ─────────────────────────────────────────────────

_BARS = {
    "purple": ("🟣", "⚪"),   # стиль пользователя
    "pink":   ("🩷", "🤍"),   # онбординг, приветствие
    "yellow": ("🟡", "⚪"),   # прогресс, рост
    "green":  ("🟢", "⚪"),   # успех, готово
    "red":    ("🔴", "⚪"),   # срочность
    "blue":   ("🔵", "⚪"),   # нейтральный
    "orange": ("🟠", "⚪"),   # триал, предупреждение
}


def _bar(current: int, total: int, color: str = "purple", width: int = 5) -> str:
    """Базовый цветной бар. Ширина 5 — оптимально для мобильного."""
    fill, empty = _BARS.get(color, _BARS["purple"])
    filled = round(width * current / total) if total > 0 else 0
    filled = max(0, min(width, filled))
    return fill * filled + empty * (width - filled)


def _pct_bar(pct: float, color: str = "blue", width: int = 5) -> str:
    """pct = 0.0..1.0"""
    fill, empty = _BARS.get(color, _BARS["blue"])
    filled = round(width * pct)
    filled = max(0, min(width, filled))
    return fill * filled + empty * (width - filled)


# ── Твой стиль — 4 уровня с цветом ───────────────────────────────────────────

def voice_progress(total_signals: int) -> str:
    """
    Полный прогресс-бар стиля пользователя для кабинета.

    Уровни:
      0       → не начато
      1–9     → 🩷 розовый  «Начинаю изучать»
      10–24   → 🟣 фиолетовый «Изучаю»
      25–49   → 🟡 жёлтый  «Хорошо знаю»
      50+     → 🟢 зелёный «Знаю хорошо»

    Нет магического порога — чем больше, тем точнее всегда.
    """
    n = total_signals

    if n == 0:
        empty = "🤍" * 5
        return (
            "✨ *Мой стиль*\n"
            f"{empty} ещё не изучен\n\n"
            "_Скинь свои посты или оценивай результаты — "
            "начну писать в твоей манере._"
        )
    elif n < 10:
        bar = _bar(n, 10, "pink")
        return (
            f"✨ *Мой стиль* — начинаю изучать\n"
            f"{bar} {n} сигналов\n\n"
            "_Чем больше оцениваешь и добавляешь постов — тем точнее становлюсь._"
        )
    elif n < 25:
        bar = _bar(n - 10, 15, "purple")
        done = "🟣" * 5
        return (
            f"✨ *Мой стиль* — изучаю\n"
            f"{done} {bar} {n} сигналов\n\n"
            "_Уже вижу твою манеру. Продолжай оценивать — правок будет меньше._"
        )
    elif n < 50:
        done = "🟣" * 5 + " " + "🟡" * 5
        bar  = _bar(n - 25, 25, "yellow")
        return (
            f"✨ *Мой стиль* — хорошо знаю\n"
            f"{done} {bar} {n} сигналов\n\n"
            "_Пишу в твоей манере. Каждая оценка делает тексты ещё точнее._"
        )
    else:
        return (
            f"✨ *Мой стиль* — знаю хорошо 🟢\n"
            f"🟢🟢🟢🟢🟢 {n} сигналов\n\n"
            "_Сильный профиль. Продолжай оценивать — всегда есть куда расти._"
        )


def voice_progress_short(total_signals: int) -> str:
    """Инлайн-версия: после генерации и в фидбеке."""
    n = total_signals
    if n == 0:
        return "\n\n_Оцени — начну запоминать как ты пишешь 🤍_"
    elif n < 10:
        bar = _bar(n, 10, "pink")
        return f"\n\n_Изучаю твой стиль: {bar} {n} сигналов. Чем больше оценок — тем точнее._"
    elif n < 25:
        return f"\n\n_Твой стиль: {n} сигналов 🟣 — уже вижу твою манеру._"
    elif n < 50:
        return f"\n\n_Твой стиль: {n} сигналов 🟡 — хорошо знаю как ты пишешь._"
    else:
        return f"\n\n_Твой стиль: {n} сигналов 🟢 — сильный профиль._"


