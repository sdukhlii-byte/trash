# ── Онбординг-прогресс ────────────────────────────────────────────────────────

def onboarding_progress(step: int, total: int = 3) -> str:
    """Шаг онбординга: 'Шаг 1 из 3 [▓▓▓░░░░░] 33%'"""
    bar = "▓" * step + "░" * (total - step)
    pct = round(step / total * 100)
    return f"_Шаг {step} из {total} [{bar}] {pct}%_"


# ── Триал-таймер ──────────────────────────────────────────────────────────────

def trial_urgency(hours_left: float) -> str:
    """Визуальный индикатор срочности триала."""
    if hours_left <= 6:
        return f"🔴 *Осталось {round(hours_left)} ч.* — триал заканчивается скоро!"
    elif hours_left <= 24:
        return f"🟡 *Остался {round(hours_left)} ч.* до конца триала."
    else:
        days = round(hours_left / 24)
        bar = "▓" * min(days, 7) + "░" * max(0, 7 - days)
        return f"⏳ Триал: [{bar}] {days} дн."
