"""
ui/progress_bar.py — единая библиотека прогресс-баров и живых статусов.

Используется везде: кабинет, голос, генерация, онбординг.
Принцип: каждое ожидание должно что-то показывать. Каждый прогресс — виден.
"""

# ── Прогресс-бар (универсальный) ─────────────────────────────────────────────

def progress_bar(current: int, total: int, width: int = 8,
                 fill: str = "▓", empty: str = "░") -> str:
    """
    Возвращает строку вида [▓▓▓▓░░░░] 4/8
    current=0 → [░░░░░░░░] 0/8
    current=total → [▓▓▓▓▓▓▓▓] 8/8
    """
    filled = round(width * current / total) if total > 0 else 0
    filled = max(0, min(width, filled))
    bar = fill * filled + empty * (width - filled)
    return f"[{bar}] {current}/{total}"


def percent_bar(pct: float, width: int = 8,
                fill: str = "▓", empty: str = "░") -> str:
    """pct=0.0..1.0 → [▓▓▓▓░░░░] 50%"""
    filled = round(width * pct)
    filled = max(0, min(width, filled))
    bar = fill * filled + empty * (width - filled)
    return f"[{bar}] {round(pct * 100)}%"


# ── Голос Миры ────────────────────────────────────────────────────────────────

def voice_progress(total_signals: int) -> str:
    """
    Многоуровневый прогресс настройки голоса.
    Показывает текущий уровень и что будет дальше.

    0      → пустой бар + приглашение
    1-4    → заполняется до уровня 5 «стабильный голос»
    5-9    → заполняется до уровня 10 «точный голос»
    10-19  → заполняется до уровня 20 «идеальное попадание»
    20+    → максимум, «Мира знает тебя наизусть»
    """
    n = total_signals

    if n == 0:
        return (
            "🎙 *Голос Миры*\n"
            "[░░░░░░░░] не настроен\n\n"
            "_Оцени первый результат — и Мира начнёт запоминать твой стиль._"
        )
    elif n < 5:
        bar = progress_bar(n, 5)
        return (
            f"🎙 *Голос Миры* — уровень 1: Знакомство\n"
            f"{bar}\n\n"
            f"_Ещё {5 - n} оценки — и Мира начнёт попадать в твой голос стабильно._"
        )
    elif n < 10:
        bar = progress_bar(n - 5, 5)
        return (
            f"🎙 *Голос Миры* — уровень 2: Стабильный\n"
            f"[▓▓▓▓▓] ✓  {bar}\n\n"
            f"_Ещё {10 - n} оценок до уровня «Точный» — меньше правок на каждый текст._"
        )
    elif n < 20:
        bar = progress_bar(n - 10, 10)
        return (
            f"🎙 *Голос Миры* — уровень 3: Точный\n"
            f"[▓▓▓▓▓] ✓  [▓▓▓▓▓] ✓  {bar}\n\n"
            f"_Ещё {20 - n} оценок до уровня «Идеальный» — тексты с первого раза._"
        )
    else:
        return (
            "🎙 *Голос Миры* — уровень 4: Идеальный ✨\n"
            "[▓▓▓▓▓] ✓  [▓▓▓▓▓] ✓  [▓▓▓▓▓▓▓▓▓▓] ✓\n\n"
            f"_Мира знает тебя наизусть — {n} сигналов в базе._"
        )


def voice_progress_short(total_signals: int) -> str:
    """
    Короткая версия для inline-сообщений (после генерации, в фидбеке).
    """
    n = total_signals
    if n == 0:
        return "\n\n_Оцени — и Мира запомнит твой стиль._"
    elif n < 5:
        bar = "▓" * n + "░" * (5 - n)
        return f"\n\n_Голос Миры: [{bar}] {n}/5 — ещё {5 - n} до стабильного попадания._"
    elif n < 10:
        bar = "▓" * (n - 5) + "░" * (10 - n)
        return f"\n\n_Голос Миры: [▓▓▓▓▓][{bar}] уровень 2 — точность растёт. 🎯_"
    elif n < 20:
        return f"\n\n_Голос Миры: уровень 3 «Точный» ({n} сигналов) 🎯_"
    else:
        return f"\n\n_Голос Миры: уровень 4 «Идеальный» ✨ ({n} сигналов)_"


