"""
ui/progress_bar.py — прогресс-бары и живые статусы.

v3: нарратив исправлен — «твой стиль», не «голос Миры».
Пользователь должен понимать: бот учится писать КАК ОН, а не настраивает себя.

Палитра по контексту:
  🟣 фиолетовый  — стиль/голос пользователя (творчество, личность)
  🩷 розовый     — онбординг (тепло, приветствие)
  🟡 жёлтый      — прогресс/рост (энергия, движение)
  🟢 зелёный     — завершение/успех
  🔴 красный     — срочность/дедлайн
  🔵 синий       — нейтральный прогресс
"""

# ── Универсальные эмодзи-сеты ─────────────────────────────────────────────────

_BARS = {
    "purple": ("🟣", "⚪"),   # стиль пользователя
    "pink":   ("🩷", "🤍"),   # онбординг, приветствие
    "yellow": ("🟡", "⚪"),   # прогресс, рост
    "green":  ("🟢", "⚪"),   # успех, готово
    "red":    ("🔴", "⚪"),   # срочность
    "blue":   ("🔵", "⚪"),   # нейтральный
    "orange": ("🟠", "⚪"),   # триал, предупреждение
}


def _bar(current: int, total: int, color: str = "purple", width: int = 5) -> str:
    """Базовый цветной бар. Ширина 5 — оптимально для мобильного."""
    fill, empty = _BARS.get(color, _BARS["purple"])
    filled = round(width * current / total) if total > 0 else 0
    filled = max(0, min(width, filled))
    return fill * filled + empty * (width - filled)


def _pct_bar(pct: float, color: str = "blue", width: int = 5) -> str:
    """pct = 0.0..1.0"""
    fill, empty = _BARS.get(color, _BARS["blue"])
    filled = round(width * pct)
    filled = max(0, min(width, filled))
    return fill * filled + empty * (width - filled)


# ── Твой стиль — 4 уровня с цветом ───────────────────────────────────────────

def voice_progress(total_signals: int) -> str:
    """
    Полный прогресс-бар стиля пользователя для кабинета.

    Уровни:
      0       → пустой, приглашение оценить
      1–4     → 🩷 розовый  «Знакомлюсь»
      5–9     → 🟣 фиолетовый «Узнаю тебя»
      10–19   → 🟡 жёлтый  «Пишу как ты»
      20+     → 🟢 зелёный «Твой голос точно»
    """
    n = total_signals

    if n == 0:
        empty = "🤍" * 5
        return (
            "🎙 *Твой стиль*\n"
            f"{empty} ещё не изучен\n\n"
            "_Оцени первый результат — и я начну запоминать как ты пишешь._"
        )
    elif n < 5:
        bar = _bar(n, 5, "pink")
        return (
            f"🎙 *Твой стиль* — знакомлюсь\n"
            f"{bar} {n}/5\n\n"
            f"_Ещё {5 - n} {'оценка' if 5-n==1 else 'оценки'} — и начну попадать в твой голос стабильно._"
        )
    elif n < 10:
        done  = "🟣" * 5
        bar   = _bar(n - 5, 5, "purple")
        return (
            f"🎙 *Твой стиль* — узнаю тебя\n"
            f"{done} ✓  {bar} {n}/10\n\n"
            f"_Ещё {10 - n} до уровня «Пишу как ты» — тексты будут требовать меньше правок._"
        )
    elif n < 20:
        done  = "🟣" * 5 + " ✓  " + "🟡" * 5 + " ✓  "
        bar   = _bar(n - 10, 10, "yellow")
        return (
            f"🎙 *Твой стиль* — пишу как ты\n"
            f"{done}{bar} {n}/20\n\n"
            f"_Ещё {20 - n} до уровня «Точно» — тексты с первого раза._"
        )
    else:
        return (
            f"🎙 *Твой стиль* — точно ✨\n"
            f"🟢🟢🟢🟢🟢 {n} примеров\n\n"
            "_Знаю тебя хорошо. Каждая оценка делает тексты ещё точнее._"
        )


def voice_progress_short(total_signals: int) -> str:
    """Инлайн-версия: после генерации и в фидбеке."""
    n = total_signals
    if n == 0:
        return "\n\n_Оцени — и я начну запоминать твой стиль 🤍_"
    elif n < 5:
        bar = _bar(n, 5, "pink")
        return f"\n\n_Учу твой стиль: {bar} {n}/5 — ещё {5-n} до стабильного попадания._"
    elif n < 10:
        bar = _bar(n - 5, 5, "purple")
        return f"\n\n_Твой стиль: 🟣🟣🟣🟣🟣 {bar} — узнаю тебя лучше 🎯_"
    elif n < 20:
        return f"\n\n_Твой стиль: уровень «Пишу как ты» 🟡 ({n} примеров) — почти идеально._"
    else:
        return f"\n\n_Твой стиль: точно 🟢✨ ({n} примеров)_"


